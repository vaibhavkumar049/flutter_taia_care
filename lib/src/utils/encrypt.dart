class Encrypt{
  static String encryptEmail(String email){
    String encryptedEmail = email;
    return encryptedEmail;
  }

  static String encryptPassword(String password){
    String encryptedPassword = password;
    return encryptedPassword;
  }

  static String encryptConnectionCode(String code){
    String encryptedConnectionCode = code;
    return encryptedConnectionCode;
  }
  static String encryptGender(String gender){
    String encryptedGender = gender;
    return encryptedGender;
  }
}